package com.index;

public class AssignementOperator {

	public static void main(String[] args) {
		double salary = 20000;
		
		salary += 1000;
		System.out.println("Total salary" + salary);
		
		salary -= 500;
		System.out.println("Total salary" + salary);
		
		salary *= 3;
		System.out.println("Total salary" + salary);
		
		salary /=5;
		System.out.println("Total salary" + salary);

	}

}
